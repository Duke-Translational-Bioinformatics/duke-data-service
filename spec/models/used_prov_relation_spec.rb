require 'rails_helper'

RSpec.describe UsedProvRelation, type: :model do
  subject { FactoryBot.create(:used_prov_relation) }
  let(:resource_serializer) { UsedProvRelationSerializer }
  let(:expected_relationship_type) { 'used' }

  it_behaves_like 'a ProvRelation' do
    let(:expected_kind) { 'dds-relation-used' }
    let(:serialized_kind) { true }
    let(:kinded_class) { UsedProvRelation }
  end

  include_context 'mock all Uploads StorageProvider'
  
  describe 'validations' do
    include_context 'performs enqueued jobs', only: GraphPersistenceJob
    it { is_expected.to allow_value('Activity').for(:relatable_from_type) }
    it { is_expected.not_to allow_value('Project').for(:relatable_from_type) }
    it { is_expected.to allow_value('FileVersion').for(:relatable_to_type) }
    it { is_expected.not_to allow_value('Project').for(:relatable_to_type) }

    context 'already generated by activity' do
      let(:used_and_generated_by) { FactoryBot.create(:activity) }
      let(:used_and_generated) { FactoryBot.create(:file_version) }
      subject { FactoryBot.build(:used_prov_relation,
        relatable_to: used_and_generated,
        relatable_from: used_and_generated_by)
      }
      context 'not deleted' do
        let(:already_generated) { FactoryBot.build(:generated_by_activity_prov_relation,
          relatable_to: used_and_generated_by,
          relatable_from: used_and_generated)
        }
        it {
          is_expected.to be_valid
          already_generated.save
          is_expected.not_to be_valid
        }
      end

      context 'deleted' do
        let(:already_generated) { FactoryBot.build(:generated_by_activity_prov_relation,
          relatable_to: used_and_generated_by,
          relatable_from: used_and_generated,
          is_deleted: true)
        }
        it {
          is_expected.to be_valid
          already_generated.save
          is_expected.to be_valid
        }
      end
    end
  end
end
